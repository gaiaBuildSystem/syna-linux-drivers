inline void hrx_reg_write(struct syna_hrx_v4l2_dev *hrx_dev, u32 val, u32 reg)
{
	HRX_REG_WORD32_WRITE((hrx_dev->hrx_base + reg), val);
}

inline u32 hrx_reg_read(struct syna_hrx_v4l2_dev *hrx_dev, u32 reg)
{
	u32 val;

	HRX_REG_WORD32_READ((hrx_dev->hrx_base + reg), &val);
	return val;
}

inline void hrx_reg_mask_write(struct syna_hrx_v4l2_dev *hrx_dev, u32 data, u32 reg,
	u32 shift, u32 mask)
{
	u32 val = hrx_reg_read(hrx_dev, reg) & ~mask;

	val |= (data << shift) & mask;
	hrx_reg_write(hrx_dev, val, reg);
}

inline u32 hrx_reg_mask_read(struct syna_hrx_v4l2_dev *hrx_dev, u32 reg, u32 shift,
	u32 mask)
{
	return (hrx_reg_read(hrx_dev, reg) & mask) >> shift;
}

inline u32 hrx_reg_get_int_val(struct syna_hrx_v4l2_dev *hrx_dev, u32 stat_reg,
	u32 mask_reg)
{
	return hrx_reg_read(hrx_dev, stat_reg) & hrx_reg_read(hrx_dev, mask_reg);
}

inline void vip_reg_write(struct syna_hrx_v4l2_dev *hrx_dev, u32 reg, u32 val)
{
	HRX_REG_WORD32_WRITE((hrx_dev->vip_base + reg), val);
}

inline u32 vip_reg_read(struct syna_hrx_v4l2_dev *hrx_dev, u32 reg)
{
	u32 val;

	HRX_REG_WORD32_READ((hrx_dev->vip_base + reg), &val);
	return val;
}

inline void glb_reg_write(u32 reg, u32 val)
{
	HRX_REG_WORD32_WRITE(reg, val);
}

inline u32 glb_reg_read(u32 reg)
{
	u32 val;

	HRX_REG_WORD32_READ(reg, &val);
	return val;
}

void hrx_dump_reg(struct seq_file *s)
{
	int size, idx;
	u32 regVal;

	seq_printf(s, "********************* HRX REG DUMP *********************\n");
	size = sizeof(hrx_reg_table)/sizeof(HRX_REG_INFO);
	for (idx = 0; idx < size; idx++) {
		regVal = glb_reg_read(HDMIRX_PIPE_REG_BASE + hrx_reg_table[idx].addr);
		seq_printf(s, "%-60s 0x%08X 0x%X\n", hrx_reg_table[idx].addr_name, HDMIRX_PIPE_REG_BASE + hrx_reg_table[idx].addr, regVal);
	}
}

